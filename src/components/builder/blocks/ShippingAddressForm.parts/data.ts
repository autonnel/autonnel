export function emptyAddressData(): AddressData {
  return {
    email: '',
    firstName: '',
    lastName: '',
    phone: '',
    addressSearch: '',
    address1: '',
    address2: '',
    city: '',
    state: '',
    postalCode: '',
    country: 'US',
  };
}

export function isAddressComplete(data: AddressData): boolean {
  return Boolean(
    data.email &&
      data.firstName &&
      data.lastName &&
      data.address1 &&
      data.city &&
      data.state &&
      data.postalCode &&
      data.country,
  );
}

